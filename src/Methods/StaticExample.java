public class StaticExample {

    public static void main(String[] args) {
        
   
        
        double squareRoot = Math.sqrt(25); // Finds the square root
        int absoluteValue = Math.abs(-50); // Turns negative to positive
        
        System.out.println("Square root of 25 is: " + squareRoot);
        System.out.println("Absolute value of -50 is: " + absoluteValue);
    }
}